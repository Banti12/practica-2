    public String [][] calcular_fila ()
    {   
        uno= new DefaultTableModel();
        String [][] salida = null ; 
        double bytes = 0;
        dentro = new conectar(nombrebase.getText());
        try {
           int x = dentro.num_tablas();
           String [] nombres_tablas = new String [x];
           nombres_tablas=dentro.tablas(x);
           salida = new String [x][2];
           for(int i =0;i<x;i++)
           {
            int x1 = dentro.num_columnas(nombres_tablas[i]);             
            salida[i][0]=nombres_tablas[i]; 
            bytes=dentro.tipo(nombres_tablas[i], x1); 
            salida[i][1]= String.valueOf(bytes);             
           }
        } catch (SQLException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        return salida;
    }

