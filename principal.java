    private void buscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscarMouseClicked
       if(nombrebase.getText().equals("") || pagina.getText().equals("") || registros.getText().equals(""))
       {
           error.setVisible(true);
       }else
       {
           error.setVisible(false);
            String [][] salida =calcular_fila();
            String resultado="Nombre                 Bytes \n";
            double bytestotales=0;
            for(int i = 0; i<salida.length;i++)
            {
                for(int j=0;j<2;j++)
                {
                    resultado+=salida[i][j];
                    resultado+="                 ";
                    if(j==1)
                    {
                      bytestotales += Double.parseDouble(salida[i][j]);
                    }
                }
                resultado+="\n";
            }
            resultado+="\n";
            resultado+="\n";
            resultado+="suma total de Filas: "+bytestotales;
            double pag = bytestotales*Double.parseDouble(pagina.getText());
            resultado+="\n";
            resultado+="\n";
            resultado+="tamaño de paginas totales: "+pag;
            double reg = pag*(Double.parseDouble(registros.getText())/Double.parseDouble(pagina.getText()));
            resultado+="\n";
            resultado+="\n";
            resultado+="tamaño de la base de datos: "+reg;
            double calcular_gigas = (reg*1)/1073741824;
            resultado+="\n";
            resultado+="\n";
            resultado+="tamaño de la base de datos en gigabytes: "+calcular_gigas;
            CalculoDeFilas.setText(resultado);
       }
       
    }//GEN-LAST:event_buscarMouseClicked

